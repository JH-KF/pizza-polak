export const orderType = defineType({
  name: 'order',
  title: 'Commande',
  type: 'document',
  fields: [
    defineField({
      name: 'order_name',
      title: 'Nom de la commande',
      type: 'string',
      validation: (v) => v.required(),
    }),
    defineField({
      name: 'order_time',
      title: 'Heure de commande',
      type: 'string',
      options: {list: ORDER_TIMES},
      validation: (v) => v.required(),
    }),
    defineField({
      name: 'order_items',
      title: 'Liste des pizza',
      type: 'array',
      of: [{type: 'orderItem'}],
    }),
  ],
  preview: {
    select: {
      orderName: 'order_name',
      orderTime: 'order_time',
      orderItems: 'order_items',
    },
    prepare(selection) {
      const {orderName, orderItems, orderTime} = selection
      return {
        title: `${orderName}`,
        subtitle: `${orderTime || ' ⚠️ Horaire à définir'} - Pizza : ${orderItems?.length || 0}`,
        media: '',
      }
    },
  },
})
